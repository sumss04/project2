package com.usa.thirdjava.sum;

public class TestEncapsualtion {
	//parent=common properties
	//variable will be private
	//method will be public
	//method=getter(),setter()
	//getter=to get something
	//setter=to set something
	
	private int a;
	private String b;
	private double c;
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public double getC() {
		return c;
	}
	public void setC(double c) {
		this.c = c;
	}

}
